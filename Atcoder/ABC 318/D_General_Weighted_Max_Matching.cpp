vector<vl>adj(17,vl(17,0));
vector<vl>dp(1<<17,vl(17,-1));
ll n;
ll recur(ll ind, ll val){
     if(ind>=n) return 0;
     if(dp[val][ind]!=-1) return dp[val][ind];
     ll ans=0;
     
     ans=max(ans,recur(ind+1,val));
     
     if(!(val&(1<<ind))){
          for(int i=ind+1;i<n;i++){
               if(val&(1<<i)) continue;
               ans=max(ans,adj[ind][i]+recur(ind+1,(1<<i)|val));
          }
     }
     return dp[val][ind]=ans;
}

