vi v,ans;

void dfs(int node, int par,vi &seq){
     

     pair<int,int> p={-1,-1};

     if(seq.size()==0){
          seq.pb(v[node]);
     }else{
          auto it=lower_bound(all(seq),v[node]);
          if(it==seq.end()){
               seq.pb(v[node]);
          }else{
               int idx=it-seq.begin();
               p={idx,seq[idx]};
               seq[idx]=v[node];
          }
     }

     ans[node]=seq.size();
     for(auto i:adj[node]){
          if(i==par) continue;
          dfs(i,node,seq);
     }
     if(p.first==-1){
          seq.pop_back();
     }else{
          seq[p.first]=p.second;
     }
}

