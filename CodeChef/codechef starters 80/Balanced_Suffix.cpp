void dk(){
     ll n, k;
     cin>>n>>k;
     string s;
     cin>>s;
     map<ll,ll> cnt;
     for(auto it:s) cnt[it-'a']++;

     string ans="";

     for(int _=0;_<n;_++){
        
        ll mx=-1;
        for(int j=26;j>=0;j--){
            if(cnt[j]!=0){
                mx=j;
                break;
            }
        }
        if(mx==-1) break;
        // debug(cnt);
        ll ct=k;
        while(ct && cnt[mx]){
            ans+=mx+'a';
            cnt[mx]--;
            ct--;
        }
        for(int i=mx-1;i>=0;i--){
            if(cnt[i]>0){
                ans+=i+'a';
                cnt[i]--;
            }
        }

     }

     for(int i=0;i<26;i++) cnt[i]=0;
     for(auto it:s) cnt[it-'a']++;
     map<ll,ll> ct;

     for(auto it:ans){
        ct[it-'a']++;
        for(int i=0;i<26;i++){
            for(int j=i+1;j<26;j++){
                if(cnt[i]!=0 && cnt[j]!=0 && abs(ct[i]-ct[j])>k){
                    cout<<-1<<endl;
                    return;
                }
            }
        }

     } 


     reverse(all(ans));
     cout<<ans<<endl;
}




