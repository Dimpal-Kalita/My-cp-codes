void dk(){
     ll n;
     cin>>n;
     if(n==2){
          cout<<"-1"<<endl;
          return;
     }
     vector<vl>ans(n,vector<ll>(n,0));
     ans[0][0]=1;
     ll o=3;
     for(int i=0;i<n;i++){
          if(i%2) continue;
          for(int j=0;j<i;j++){
               ans[i][j]=o;
               o+=2;
               ans[j][i]=o;
               o+=2;
          }
     }
     if(n%2==0){
          swap(ans[n-2][0],ans[n-2][n-2]);
          for(int j=0;j<n;j++){
               if(o>n*n) break;
               ans[n-1][j]=o;
               o+=2;
               if(o>n*n) break;
               ans[j][n-1]=o;
               o+=2;
          }
     }
     ll e=2;
     for(int i=0;i<n;i++){
          for(int j=0;j<n;j++){
               if(ans[i][j]==0){
                    ans[i][j]=e;
                    e+=2;
               }
          }
     }
     for(int i=0;i<n;i++){
          for(int j=0;j<n;j++){
               cout<<ans[i][j]<<" ";
          }
          cout<<endl;
     }
}



