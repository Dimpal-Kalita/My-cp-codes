void dk(){
     ll n,q;
     cin>>n>>q;
     vector<ll> v(n);
     inp(v);
     map<ll,ll> L,R;
     while(q--){
          ll l,r;
          cin>>l>>r;
          l--,r--;
          L[l]++,R[r]++;
     }

     ll x=0;
     vector<pll> ans(n);
     for(int i=0;i<n;i++){
          x+=L[i];
          ans[i]={x,i};
          x-=R[i];
     }
     debug(ans);
     sort(all(ans));
     sort(all(v));
     vector<ll> ret(n);
     x=0;
     for(int i=0;i<n;i++){
          ret[ans[i].S]=v[i];
          x+= v[i]*ans[i].F;
     }

     cout<<x<<endl;
     for(int i=0;i<n;i++){
          cout<<ret[i]<<" ";
     }
     cout<<endl;
}



