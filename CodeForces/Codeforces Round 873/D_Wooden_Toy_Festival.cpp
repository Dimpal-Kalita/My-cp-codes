void dk(){
     ll n;
     cin>>n;
     vector<ll>v(n);
     inp(v);
     sort(all(v));

     auto check=[&](ll x)->bool{
          vector<pll> vp;
          for(int i=0;i<n;i++){
               ll mn=v[i]-x;
               ll mx=v[i]+x;
               bool flag=0;
               for(auto &it:vp){
                    if((it.S>=mn and it.S<=mx)|| (it.F>=mn and it.F<=mx)){
                         it.F=max(it.F,mn);
                         it.S=min(it.S,mx);
                         flag=1;
                    }
               }
               if(!flag){
                    vp.pb({mn,mx});
               }
          }
          return sz(vp)<=3;
     };

     ll l=0,r=1e9+7;
     while(l<r){
          ll mid=(r-l)/2+l;
          if(check(mid)){
               r=mid;
          }
          else{
               l=mid+1;
          }
     }
     cout<<l<<endl;
}



