void dk(){
     ll n;
     cin>>n;
     ll l=1,r=1e18+3;
     ll ans=0;
     while(l<r){
          __int128 mid=(r-l)/2+l;
          __int128 val=(mid*(mid-1))/2;
          if(val<=n){
               ans=mid;
               l=mid+1;
          }else{
               r=mid;
          }
     }
     ans+=(n-((ans*(ans-1))/2));
     cout<<ans<<endl;
}



