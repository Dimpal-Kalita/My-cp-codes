vector<vector<pll>>vp;

void dk(){
     ll n;
     cin>>n;
     vl v(n);
     inp(v);
     vp.clear();
     vp.resize(32);
     ll sum=accumulate(all(v),0LL);
     if(sum%n){
          cout<<"No"<<endl;
          return;
     }

     vl count(32,0);
     ll avg=sum/n;
     
     auto calc=[&](ll p){
          ll x=abs(p-avg);
          pll ans={-1,-1};
          for(int i=0;i<31;i++){
               if((1<<i)>x){
                    ll rem=(1<<i)-x;
                    if(__builtin_popcount(rem)==1){
                         ans={i,log2(rem)};
                         break;
                    }
               }
          }
          if(p>avg){
               swap(ans.F,ans.S);
               return ans;
          }
          return ans;
     };

     for(auto i:v){
          if(i==avg) continue;
          pll ans=calc(i);
          if(ans.F==-1){
               cout<<"No"<<endl;
               return;
          }
          if(__builtin_popcount(i)==0){
          }
          count[ans.F]++;
          count[ans.S]--;
     }
     if(count!=vl(32,0)){
          cout<<"No"<<endl;
          return;
     }
     cout<<"Yes"<<endl;
}



