void dk(){
     ll n;
     cin>>n;
     map<ll,ll> mp;
     ll x= n;
     for(int i=2;i*i<=x;i++){
        while(x%i==0){
            x/=i;
            mp[i]++;
        }
     }
     if(x>1) mp[x]++;



     vector<ll> ans(40, 1), vis(40, 0);
     for(auto it:mp){
        ll ct=it.S;
        while(ct>0){
            ans[ct]*=it.F;
            vis[ct]=1;
            ct--;
        }
     }
     ll val=0;
     for(int i=0;i<40;i++){
        if(vis[i]) val+=ans[i]; 
     }
     cout<<val<<endl;

}




