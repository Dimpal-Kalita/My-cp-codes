void dk(){
     ll n;
     cin>>n;
     vector<ll> v1(n), v2(n);
     inp(v1);
     inp(v2);

     multiset<ll> ml;
     vector<ll> ans(n,0);
     ll sum=0;

     for(int i=0;i<n;i++){
        ml.insert(v1[i]+sum);
        sum+=v2[i];
       
        vector<ll> ind;
        
        for(auto it:ml){
            if(it>sum) break;
            
            ind.pb(it);
            ans[i]+= it-(sum-v2[i]);

        }

        ans[i]+= (ml.size()-ind.size())*v2[i]; 
    
        for(auto it:ind){
            ml.erase(ml.find(it));
        }

     }
    
    for(auto i:ans){
        cout<<i<<" ";
    }cout<<endl;
}




