void dk(){
     ll n;
     cin>>n;
     vl v(n);
     inp(v);
     ll sum=accumulate(all(v),0LL);
     vector<vl> dp(n,vl(sum+1,-1));
     function<ll(ll,ll)> solve=[&](ll i,ll s){
          if(i==n) return abs(sum-2*s);
          if(dp[i][s]!=-1) return dp[i][s];
          return dp[i][s]=min(solve(i+1,s+v[i]),solve(i+1,s));
     };
     cout<<solve(0,0)<<endl;
}



