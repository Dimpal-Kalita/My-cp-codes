class FriendFinder {
  public:
  int distance(string line) {
      ll start=0, end=0;
	  for(ll i=0;i<line.size();i++){
		  if(line[i]=='S' || line[i]=='F'){
			  start=i;
			  break;
		  }
	  }
	  for(ll i=line.size()-1;i>=0;i--){
		  if(line[i]=='S' || line[i]=='F'){
			  end=i;
			  break;
		  }
	  }
	  return end-start;
  }
};

