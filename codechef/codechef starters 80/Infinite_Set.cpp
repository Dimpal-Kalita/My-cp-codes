vector<ll> dp;




ll func(ll i){
	if(i<0) return 0;
	if(dp[i]!=0) return dp[i];
	return dp[i]=func(i-1)+func(i-2)+mp[i];
}

ll recur(ll i){
	if(i<0) return 0;

	ll ans=0;
	ans+= func(i)+recur(i-1);

	return ans;
}


